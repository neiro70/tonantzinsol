		<!-- Intro Text -->

		<div class="item_introtext">
			<p align="justify"> Definiéndola en un solo párrafo, lo más relevante sería decir que se trata de una Psicología que fue cobrando cuerpo desde fines de la década de 1960, como continuación natural de la Psicología Humanista (Maslow, Rogers, Frankl y otros), haciendo hincapié (en sus conceptos y sus técnicas) en ayudar al mayor despliegue de la persona, incluyendo en ello su búsqueda de un Sentido Trascendente en relación a la realidad y a su propia vida.</p>
			<p align="justify"> Recordemos que la Psicología nació del estudio de personas que padecían severas neurosis o psicosis; eso dio pie a que los posteriores desarrollos de esa disciplina se configuraran a partir de una mirada patologizante de la interioridad humana. El Movimiento Humanista empezó por subrayar que definir el mapa del psiquismo partiendo de la enfermedad distorsionaba la mirada hacia el otro; que, así como existía una Psicopatología, debía estudiarse la Psicología del Bienestar y del Desarrollo, e investigar a las personas que, en su propio despliegue, eran capaces de construir vidas valiosas, admirables, creativas, basadas en valores del Ser (como les llamó Abraham Maslow).</p>
			<p align="justify"> Así empezó a nacer otra Psicología: otra manera de concebirse a sí mismo y a los demás, otro encuadre para trabajar en la práctica clínica (más sensible y cercano), otra apreciación de los recursos con los que un individuo puede contar para expresar su singularidad y un bagaje de herramientas para apoyar esa expresión fundamental.</p>
			<p align="justify">     En los años ’60 suceden distintos hechos que modificarían para siempre a Occidente: desde la guerra de Vietnam a los movimientos pacifistas, la invasión china al Tibet con la diáspora de sus habitantes, el advenimiento de conocimientos desde Oriente (la Meditación, el Yoga, el Tai-Chi, otro paradigma de la medicina…) fueron preparando el terreno para que aconteciera esta necesidad radical: la de incorporar a la Psicología el aspecto espiritual del ser humano (minimizado por el paradigma clásico, y muchas veces visto, inclusive, como una patología). Tan complejo sigue siendo este punto que hasta la palabra “espiritual” suena escurridiza y difícil de decir. Podemos referirnos a ella señalando la búsqueda de un Sentido Trascendente, que va más allá del intelecto y del ego, y sin el cual la vida se vuelve árida y asfixiante, o bien chata y sin vuelo.
					Esta Psicología fue formalmente instaurada por Abraham Maslow y Anthony Sutich en 1969 con la publicación del Journal of Transpersonal Psychology, seguida de la fundación de la Association for Transpersonal Psychology en California (EEUU) en 1972. Posteriormente, destacados terapeutas e investigadores fueron desarrollando sus principales conceptos. Entre ellos cabría destacar a Ken Wilber, Stanislav Grof, Frances Vaughan, Roger Walsh, Charles Tart, John Welwood, y otros.
					Dice Frances Vaughan: “La Psicología Transpersonal nació de la visión compartida de un grupo de psicólogos que se dieron cuenta de que las teorías psicológicas predominantes de la época eran demasiado estrechas para hacer justicia a todo el espectro de la potencialidad humana. Las definiciones de la salud mental se han expandido gradualmente hasta incluir estados óptimos de conciencia.” Y también: “‘Transpersonal’ significa literalmente ‘más allá de lo personal’. Con el estudio del desarrollo humano más allá del ego, la Psicología Transpersonal afirma la posibilidad de totalidad y autotrascendencia. La trascendencia se explora y manifiesta a través de la experiencia personal. Una visión Transpersonal de las relaciones humanas reconoce que existimos impregnados en un tejido de relaciones mutuamente condicionadas entre sí y con el entorno natural. Por esta razón, cualquier intento de mejorar la condición humana debe tomar en cuenta los temas globales, sociales y del entorno.” (De su artículo publicado en “La Evolución de la Conciencia”, compilado de Editorial Kairós.)
					En la configuración de la Psicología Transpersonal se fueron fusionando conocimientos y técnicas que ya habían existido en Oriente desde 3000 o más años antes de que Freud naciera: el Budismo Zen y Tibetano, el Taoísmo, el Sufimo, la Vedanta, y también la sabiduría de pueblos originarios de América, generaron técnicas y conceptos referidos a cada aspecto del psiquismo humano: las emociones, su relación con el pasado o el futuro, el vínculo con su cuerpo, sus sueños, su interacción con los demás y con una concepción más grande de lo que la vida pueda significar… Ya Carl Jung (1884-1961) había señalado la importancia de estos antiguos conocimientos para completar la visión del ser humano de este tiempo, (visión sin la cual cualquier Psicología o cualquier terapia queda truncada en su núcleo más vital).
			</p>
			<p align="justify"> Los recientes aportes de las Neurociencias, en este trayecto, han sido decisivos para lograr una constatación biológica de cómo las antiguas técnicas de Oriente tienen un valor innegable. (Valga como muestra las reuniones que el Mind & Life Institute de Estados Unidos genera para que el Dalai Lama y científicos de distintas disciplinas intercambien conocimientos, publicando sus hallazgos en libros y videos, hoy en día accesibles a quienes quieran ampliar su mapa de la interioridad humana.) Hoy en día, el enfoque Transpersonal se ha expandido por los cinco continentes, irradiándose a través de Asociaciones y Centros de Estudios en distintos países del mundo. Diferenes Universidades de avanzada han incorporado este paradigma a la currícula de sus carreras, y se ha vuelto el marco de trabajo de profesionales y científicos de las más variadas áreas, a tal punto que los congresos y convenciones que reúnen a quienes adscriben a este paradigma, convocan a terapeutas, científicos, filósofos, educadores y hasta a líderes religiosos de los más variados sectores, en consonante búsqueda de lograr un enfoque integral del conocimiento humano.</p>

